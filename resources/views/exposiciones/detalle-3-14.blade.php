                                <p>Hacia 1640, los clérigos diocesanos buscaron reforzar su presencia ante los empoderados frailes, que seguían evangelizando a los indígenas. Los obispos querían restar poder a las órdenes religiosas por negarse a acatar los decretos de los virreyes en materia pastoral, lo que les llevó a enfrentarse con ellos.</p>
                                <p>Puebla se consolidó como ciudad diocesana. Se fundaron la primera imprenta, hospitales y otros conventos tanto masculinos como femeninos, además de concluirse la catedral que estuvo en pausa durante muchos años, pero también levantar el seminario y el palacio episcopal, obras concretadas por Juan de Palafox y Mendoza, quien era fiscal de Castilla desde el Consejo de Indias.</p>
                                <p>El documento da cuenta de una autorización concedida a Palafox que oficializa su traslado desde Castilla, nombrado obispo de Puebla por el rey Felipe IV. Palafox llega a Nueva España como visitador real para atender problemas de jurisdicciones reales y eclesiásticas, corrupción en las instancias de poder, comisión a los mandatos de la Corona y las confrontaciones entre jesuitas y franciscanos con el poder central virreinal, además de la escasez de recursos económicos o pérdida de los mismos. Palafox da cuenta de todas estas cuestiones a través de informes al Consejo de Indias y cartas con el rey. Palafox es un exponente de la política regalista de Felipe IV. También se registran los nombres de las personas que acompañan a Palafox (su séquito) y fungirán como sus ayudantes una vez que tome posesión formal de la diócesis poblana.</p>
